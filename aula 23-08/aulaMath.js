//Math tem um conjunto de funções para matemática:
let maior = Math.max(13, 54, 01, 27); //retorna o maior valor
console.log(maior);

let menor = Math.min(13, 54, 01, 27); //retorna o menor valor
console.log(menor);

let arredondar = Math.round(3.1204940590); //arrendoda o valor
console.log(arredondar);

let arredondarPraCima = Math.ceil(3.1204940590); //arrendoda o valor pra cima
console.log(arredondarPraCima);

let raizQuadrada = Math.sqrt(16);//retorna a raiz quadrada do n.
console.log(raizQuadrada);